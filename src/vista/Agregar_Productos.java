    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        controlador.Producto producto;

        String nombre = jTextField_nombre.getText();
        String precio;
        Integer cantidad;
        String talla = jTextField_talla.getText();
        String color = jTextField_color.getText();
        String genero = null; 


        // Verificar si hay un género seleccionado
        if (jComboBox_genero.getItemCount() > 0) {
            genero = (String) jComboBox_genero.getSelectedItem();
        } else {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un género.");
            return; // Salir del método si no hay selección
        }
        if (verifFields()) {
            precio = jTextField_precio.getText();
            cantidad = Integer.valueOf(jTextField_cantidad.getText());
            // Crear el objeto Producto
            producto = new controlador.Producto(null, nombre, precio, cantidad, talla, color, genero);

            // Insertar el producto
            controlador.Producto.insertarProducto(producto);
        } else {
            JOptionPane.showMessageDialog(null, "Por favor, complete todos los campos requeridos.");
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

